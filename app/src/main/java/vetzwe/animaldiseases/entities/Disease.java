package vetzwe.animaldiseases.entities;

public class Disease {

    private int id;

    private String disease;

    public Disease() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDisease() {
        return disease;
    }

    public void setDisease(String disease) {
        this.disease = disease;
    }
}
